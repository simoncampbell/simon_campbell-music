        <div id="beginnings" class="widget">
            <h3>Simon's musical beginnings</h3>
            <p>
                Music is my first great passion and guitars caused mayhem with my academic career: from when I first picked one up at 16, to finally scraping a bad degree in Chemistry from Salford.
            </p>
            <p>
                Whilst I was supposed to be there, I was playing with hard rockers ‘Whitefire’ who released an EP, ‘Parades the Glory’ in 1978. I played sessions and taught guitar during the day for a time at Bury Grammar school but unsurprisingly, didn’t fit in with the ‘staff room scene’&hellip;
            </p>
            <p>
                We extensively toured the UK including the legendary Deeply Vale free festival. Whitefire eventually changed direction and renamed ‘Roadrunner’, the ‘punkesk’ R&amp;B band rocked its way around the country (again).
            </p>
        </div><!-- // .widget -->
        <div class="widget">
            <h3>Changin' up</h3>
            <p>I remorphed my musical career forming the 80’s europop band ‘Cutting Edge’ renamed ‘Little Brother’.</p>
            <p>
                Derek Lawrence (producer of Deep Purple and Wishbone Ash’s first three albums) and legendary ‘Big’ Jim Sullivan signed the band on a three-album deal with Line Records (Polydor Germany). Survival was released in 1992. Unfortunately, for personal reasons Tim left the partnership soon after release and the band dissolved.
            </p>
            <p>
                Seeking to return to my rock roots I formed ‘the Method’ with Mike Hehir (Guitar – Sad Cafe) and Mick Gilbourne (Drums – 10cc) and toured on and off for two years throughout the UK and Europe. At the same time my interest in production came to fore working with Rick Dowson (Colombia Records) and Millhause (Island Records).
            </p>
            <p>
                Millhause broke up and reformed as ‘The Disciples’ where I joined as guitar player and lead vocalist. This band made a very well regarded ‘albumette’ prompting ‘Q’ to write ‘Astounding presence and musicality’. Again the band prematurely split: real shame as at this point, it was probably the best thing I had done musically.
            </p>
        </div><!-- // .widget -->
        <div id="tribute" class="widget">
            <h3>A tribute (to the best song in the world)</h3>
            <p>
                Just for a laugh and before ‘tribute bands’ had become big, I put together an Eric Clapton tribute tour which was particularly successful.
            </p>
            <p>
                The highlight (and funniest) was Albert Halls (that’s the one in Bolton!) which we packed out for two consecutive nights. Due to the intensity of my business and diving careers. I wound down playing for a while, keeping my hand in by doing a number of charity gigs for my old keyboard player Stephen Dowd at Ram Fest and also jamming regulary at the Stanley Street Club in Accrington organised with my mate Rob Livesey&hellip;
            </p>
            <p>
                Moving to the Isle of Man, which has quite a vibrant music scene, has sparked guitar fever again and have put acoustic and electric bands together. Although I love almost all music, I am blues/rock man at heart.
            </p>
        </div><!-- // .widget -->
        <div class="widget">
            <h3>The perfect tone</h3>
            <p>
                Being a totally fanatical tone monster; I deliver regular ‘tone workshops’ running though my guitars, amplifiers and effects set-up to anyone who will listen!
            </p>
            <p>
                The piles of guitars that enrage my long suffering wife, Angela, were mostly made for me by Gordon Whittam (formally of Gordon Smith, Manchester Guitar Company and Gordy) based around Fender Telecasters and use the very fine hand built Van Weelden and Bludotone amps. I do play acoustic also and again have a nice selection, the Gibson SJ45 being most treasured.
            </p>
