            <h3>Simon&rsquo;s musical beginnings</h3>
            <p>
                Since strapping on his first guitar at the age of 16, Simon Campbell has spent his life immersed in music, a passion which has given him a wealth of experience across decades, countries and genres.
            </p>
            <p>
                In the late 1970s, while still a university student in Salford, Simon furthered his musical education with hard-rockers Whitefire. The band released an EP and toured the UK before taking a &lsquo;Punk-esque R &amp; B&rsquo; direction under their new moniker, Roadrunner.
            </p>
            <p>
                Returning to his Rock roots, he formed The Method with Mike Hehir from Sad Cafe and Mick Gilbourne, one time drummer with 10CC. Over a two-year period, the band toured throughout the UK and Europe.
            </p>
            <p>
                
            </p>
        </div><!-- // .widget -->
        <div class="widget">
            <h3>Great success</h3>
            <p>
                Commercial success came with his next venture, Little Brother, whose Europop sound saw them snapped up by Line Records (Polydor Germany). Although the band unfortunately split after one album (the well-received &lsquo;<a href="http://www.discogs.com/Tim-Wright-9-with-Little-Brother-Survival/release/2625575">Survival</a>&rsquo;), Simon was now free to pursue new challenges.
            </p>
            <p>
                Returning to his Rock roots, he formed The Method with Mike Hehir from Sad Cafe and Mick Gilbourne, one time drummer with 10CC. Over a two-year period, the band toured throughout the UK and Europe.
            </p>
            <p>
                During this time, Simon&rsquo;s interest in production led him to work with Rick Dowson and Millhause and, when the latter became The Disciples, Simon joined them as guitarist and lead vocalist. The Disciples disbanded in time but not before releasing a mini-album (favourably reviewed by Q), and leaving a lasting impression on live audiences.
            </p>
        </div><!-- // .widget -->
        <div id="tribute" class="widget">
            <h3>Player</h3>
            <p>
                Alongside his long and varied band career, Simon has also been a highly sought-after session player, collaborating with numerous high-profile musicians for both studio work and live performances.
            </p>
            <p>
                His <a href="http://blog.simoncampbell.com/blog/perma/the_big_move_to_the_isle_of_man/">move to the Isle of Man</a> in 2008 and subsequent involvement with its vibrant music scene led to a new a chapter in Simon&rsquo;s journey: firstly, the formation of blues-rockers The Very Very Bad Men and now, in 2011, the release of his first solo album, ThirtySix.
            </p>
            <p>
                The perfect showcase for Simon&rsquo;s versatility and profound musical understanding, ThirtySix harnesses a wide variety of guitar styles while being grounded in a deep-seated love of Rock and Blues.
            </p>
            <p>
                Whether writing, producing, recording or playing, Simon&rsquo;s appetite for musical exploration remains unabated and he intends to keep on running for a long time to come.
            </p>
