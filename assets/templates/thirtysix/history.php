            <h3>Studio team</h3>
            <blockquote>
                <p>Steve Boyce-Buckley has been a delight to work with as he understands how to record properly, not samples, but real instruments, using the magnificent console and real outboard. He has an empathy with the artist, which is very rare indeed. As co-producer, brass and backing vocal arranger, he has added many wonderful elements to the album. He has helped me create a very honest snapshot of me and my songs.</p>
                <p>The studio&rsquo;s &lsquo;digital&rsquo; assistants, Viktor Sigurj&oacute;nsson and Chris Taylor are delightful and engaging young men. Nothing was too much trouble. Beer, tea with Manuka honey, many trips to the mighty Co-Op.</p>
                <p>The studio manager, Martin Rhodes gave a calm air to proceedings and managed to extract me from the pooh on numerous occasions. Never over-fussing, but there when needed.</p>
                <p>Phil (maintenance man and lawnmaster) was lovely, creating a beautiful environment for us all to work.</p>
                <p>Finally, a sincere thanks to Ian Devaney and Lisa Stansfield for building and maintaining this unique, world class facility.</p>
                <cite>&mdash; Simon Campbell</cite>
