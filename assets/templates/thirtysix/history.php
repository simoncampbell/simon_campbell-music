            <h3>Musicians</h3>
            <p>
                Superlatives are inappropriate as the performances speak for themselves. Their individual pedigrees would occupy many pages, but in summary they have worked with artists such as: Lisa Stansfield, Proud Mary, Barclay James Harvest, Dare, The Christians, Amy Winehouse, Ella Fitzgerald, Quincy Jones, Stevie Wonder, Paul McCartney, Dave Gilmore, Tom Jones, Robbie Williams, Oleta Adams, Paul Heaton, The Beautiful South, Simply Red, Take That, Gorillas, Heather Small, the Earlies, Hot Chip, Plan B and King Creosote.
            </p>
            <ul>
                <li><strong>Simon Campbell</strong> on vocals and guitars</li>
                <li><strong>Kev (Charlie) Whitehead</strong> on drums</li>
                <li><strong>Steve Rowe</strong> on bass</li>
                <li><strong>Craig Fletcher</strong> on bass (Bewegen and flourishes elsewhere)</li>
                <li><strong>Christian Madden</strong> on Hammond, Wurlitzer, Mellotron, Rhodes, Clavinet, Moog Voyager and a bewildering array of Moogerfoogers</li>
                <li><strong>Lea Mullen</strong> on percussion</li>
                <li>The Brigadiers of Brass, <strong>Pat Hartley</strong> (Trombone), <strong>Malcolm Melling</strong> (Trumpet and Flugel Horn) and <strong>Dave Bishop</strong> on an astounding array of Saxophones</li> 
                <li>The Delta Sisters, <strong>Yvonne Shelton</strong> and <strong>Sue Quin</strong> on backing vocals</li>
            </p>
