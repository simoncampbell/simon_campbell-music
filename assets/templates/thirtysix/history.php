            <h3>The journey</h3>
            <p>
                Well this is it, my first solo album in thirty six years of spanking the plank. It’s not blues, it’s not rock, it’s not folk; it is an eclectic mix that will take you on my very personal musical journey. Each song is written about real people, with real lives.
            </p>
            <p>
                My whole band and engineering team have taken a lot of time and effort to make this sound as good as we can, using truly beautiful equipment and techniques. So, it wont be as loud as some of the CD's you buy: it’s not compressed into oblivion. It contains a lot of detail and spacial imaging, so sit down with a large glass of whatever it is that makes you smile and turn it on. Dance if you feel like it, cry if you need too.
            </p>
            <p>ThirtySix is an honest album and has many imperfections, but it is me, right now. I hope you enjoy it.</p>
            <p>
                To do my bit for the environment, we haven’t produced an insert but full detail about the songs, lyrics, musicians, the recording plus information about live shows can be found on this site.
            </p>
