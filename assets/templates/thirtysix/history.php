            <h3>The journey</h3>
            <p>
                Steve (Dr Beat) Boyce-Buckley has been a delight to work with as he understands how to record properly, not samples, but real instruments, using the magnificent console and real outboard. He has an empathy with the artist, which is very rare indeed.
            </p>
            <p>
                As co-producer, brass and backing vocal arranger, he has added many wonderful elements to the album. He has helped me to create a very honest snapshot of me and my songs. The comment this morning really floored me.
            </p>
            <blockquote>
                <p>
                    "There are very few records in the last 32 years that I have worked on that I am proud of both musically and technically. Simon Campbell’s forthcoming release is one of them and joins an elite team which can now be counted on one hand!"
                </p>
            </blockquote>
            <p>
                The studio’s ‘digital’ assistants, Viktor Sigurjónsson and Chris Taylor are delightful and engaging young men. Nothing was too much trouble. Beer, tea with Manuka honey, many trips to the mighty Co-Op.
            </p>
            <p>Phil (maintenance man and lawnmaster) was lovely, creating a beautiful environment for us all to work.</p>
            <p>
                The studio manager, Martin Rhodes gave a calm air to proceedings and managed to extract me from the pooh on numerous occasions. Never over-fussing, but there when needed.
            </p>
            <p>A sincere thanks to Ian Devaney and Lisa Stansfield for building and maintaining this unique, world class facility.</p>
