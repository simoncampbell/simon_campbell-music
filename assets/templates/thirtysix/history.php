        <h2 class="hide">ThirtySix</h2>
        <div id="thejourney" class="widget">
            <h3>The journey</h3>
            <p>
                Well this is it, Simon&rsquo;s first solo album in thirty six years of spanking the plank. It&rsquo;s not blues, it&rsquo;s not rock, it&rsquo;s not folk; it is an eclectic mix that will take you on his very personal musical journey. Each song is written about real people, with real lives.
            </p>
            <p>
                His whole band and engineering team have taken a lot of time and effort to make this sound as good as he can, using truly beautiful equipment and techniques. So, it wont be as loud as some of the CD&rsquo;s you buy: it&rsquo;s not compressed into oblivion. It contains a lot of detail and spacial imaging, so sit down with a large glass of whatever it is that makes you smile and turn it on. Dance if you feel like it, cry if you need too.
            </p>
            <p>
                ThirtySix is an honest album and has many imperfections, but it is Simon Campbell, right now. We hope you enjoy it.
            </p>
            <p>
                To do our bit for the environment, Supertone Records haven&rsquo;t produced an insert but full detail about the songs, lyrics, musicians, the recording plus information about live shows can be found on this site. Further details and links to musicians and studio team can be found at <a href="http://www.discogs.com/Simon-Campbell-ThirtySix/release/2600904">Discogs</a> and pictures taken during the recording can be found on <a href="http://www.flickr.com/photos/erskinecorp/sets/72157623649584217/">Flickr</a>.</p>
            <p>Why not read more about the making of &lsquo;ThirtySix&rsquo; on <a href="http://blog.simoncampbell.com/">Simon&rsquo;s blog</a> or <a href="http://vimeo.com/channels/simoncampbell#19387854">watch the series of videos!</a>
            </p>
